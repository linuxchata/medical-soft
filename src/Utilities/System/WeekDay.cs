namespace System
{
    /// <summary>
    /// Specifies the day of the week.
    /// Enumerator System.DayOfWeek cannot be used since it defines Sunday as first day of week.
    /// </summary>
    public enum WeekDay
    {
        /// <summary>
        /// The Monday.
        /// </summary>
        Monday = 1,

        /// <summary>
        /// The Tuesday.
        /// </summary>
        Tuesday = 2,

        /// <summary>
        /// The Wednesday.
        /// </summary>
        Wednesday = 3,

        /// <summary>
        /// The Thursday.
        /// </summary>
        Thursday = 4,

        /// <summary>
        /// The Friday.
        /// </summary>
        Friday = 5,

        /// <summary>
        /// The Saturday.
        /// </summary>
        Saturday = 6,

        /// <summary>
        /// The Sunday.
        /// </summary>
        Sunday = 7
    }
}